def generate_valid_soc_codes(section_list: tuple) -> set:
    """Generate all valid SOC codes based on section ranges and code length."""
    valid_codes = set()
    for code in section_list:
        valid_codes.update({code[:k] for k in range(1, len(code) + 1)})
    return valid_codes


